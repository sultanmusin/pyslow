package com.inr.serial;

public class HVSysException extends Exception {

	private static final long serialVersionUID = 1L;

	public HVSysException() {	
		super();
	}

	public HVSysException(String arg0) {
		super(arg0);
	}

	public HVSysException(Throwable arg0) {
		super(arg0);
	}

	public HVSysException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
